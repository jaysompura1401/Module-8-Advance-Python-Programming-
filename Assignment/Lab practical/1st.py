#Q1. Write a Python program to print a formatted string using print() and f-string.
# Taking variables
name = "Jay Sompura"
age = 22
course = "Python Programming"

# 1. Using print() with format()
print("My name is {}, I am {} years old, and I am learning {}.".format(name, age, course))

# 2. Using f-string
print(f"My name is {name}, I 1am {age} years old, and I am learning {course}.")
